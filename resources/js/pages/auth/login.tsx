import { Fragment } from "react";
import { Form, Head } from "@inertiajs/react";
import AuthLayout from "@/layouts/auth-layout";
import { register } from "@/routes";
import { store } from "@/routes/login";
import { request } from "@/routes/password";

import { Button } from "@/components/ui/button";
import { Checkbox } from "@/components/ui/checkbox";
import { Input } from "@/components/ui/input";
import { Label } from "@/components/ui/label";
import { Spinner } from "@/components/ui/spinner";
import InputError from "@/components/input-error";
import TextLink from "@/components/text-link";

interface LoginProps {
  status?: string;
  canResetPassword: boolean;
  canRegister: boolean;
}

export default function Login({ status = undefined, canResetPassword, canRegister }: LoginProps) {
  return (
    <AuthLayout
      title="Log in to your account"
      description="Enter your email and password below to log in"
    >
      <Head title="Log in" />

      <Form {...store.form()} resetOnSuccess={["password"]} className="flex flex-col gap-6">
        {({ processing, errors }) => (
          <Fragment>
            <div className="grid gap-6">
              <div className="grid gap-2">
                <Label htmlFor="email">Email address</Label>
                <Input
                  id="email"
                  type="email"
                  name="email"
                  required
                  autoFocus
                  autoComplete="email"
                  placeholder="email@example.com"
                />
                <InputError message={errors.email} />
              </div>

              <div className="grid gap-2">
                <div className="flex items-center">
                  <Label htmlFor="password">Password</Label>
                  {canResetPassword ? (
                    <TextLink href={request()} className="ml-auto text-sm">
                      Forgot password?
                    </TextLink>
                  ) : null}
                </div>
                <Input
                  id="password"
                  type="password"
                  name="password"
                  required
                  autoComplete="current-password"
                  placeholder="Password"
                />
                <InputError message={errors.password} />
              </div>

              <div className="flex items-center space-x-3">
                <Checkbox id="remember" name="remember" />
                <Label htmlFor="remember">Remember me</Label>
              </div>

              <Button
                type="submit"
                className="mt-4 w-full"
                disabled={processing}
                data-test="login-button"
              >
                {processing ? <Spinner /> : null}
                Log in
              </Button>
            </div>

            {canRegister ? (
              <div className="text-center text-sm text-muted-foreground">
                Don&lsquo;t have an account? <TextLink href={register()}>Sign up</TextLink>
              </div>
            ) : null}
          </Fragment>
        )}
      </Form>

      {status ? (
        <div className="mb-4 text-center text-sm font-medium text-green-600">{status}</div>
      ) : null}
    </AuthLayout>
  );
}
